# Conversion entre sistemas decimal, octal, binario y hexadecimal
# %%
hex(87)
# >> 0x57
oct(87)
# >> 0o127
bin(87)
# >> 0b1010111
# %%
int(0x57)
# >> 87
int(0o127)
# >> 87
int(0b1010111)
# >> 87
